export function getSiteBySlug(slug: string): SiteData | undefined {
  return sites.find(s => s.slug === slug);
}

export function getSitesByCategory(categorySlug: string): SiteData[] {
  return sites.filter(s => s.categories.includes(categorySlug));
}

export function getFeaturedSites(): SiteData[] {
  return sites.filter(s => s.is_featured && !isPendingReview(s) && !isEditorialOnly(s)).sort((a, b) => a.rank - b.rank);
}

const hasHeroBanner = (s: SiteData): boolean =>
  Boolean(getSiteImagery(s.slug).hero_image_url);

export function getPromotableSites(): SiteData[] {
  return sites.filter(s => isAffiliated(s) && hasHeroBanner(s) && !isPendingReview(s) && !isEditorialOnly(s));
}

export function getTopRatedPromotable(limit: number): SiteData[] {
  return [...getPromotableSites()]
    .sort((a, b) => b.overall_score - a.overall_score || b.update_frequency - a.update_frequency)
    .slice(0, limit);
}

/** Top-rated affiliated sites regardless of banner — for the Top 5 card section. */
export function getTopRatedAffiliated(limit: number): SiteData[] {
  return [...sites.filter(s => isAffiliated(s) && !isPendingReview(s) && !isEditorialOnly(s))]
    .sort((a, b) => b.overall_score - a.overall_score || b.update_frequency - a.update_frequency)
    .slice(0, limit);
}

export function getRecentlyUpdatedPromotable(limit: number, exclude: SiteData[] = []): SiteData[] {
  const excludeIds = new Set(exclude.map(s => s.id));
  return getPromotableSites()
    .filter(s => !excludeIds.has(s.id))
    .sort((a, b) => b.update_frequency - a.update_frequency || b.overall_score - a.overall_score)
    .slice(0, limit);
}

/**
 * Single "today's top pick" for the homepage hero direct-CTA block.
 *
 * Selection weights site quality 60% and deal depth 40%. The earlier
 * pure-multiplicative rule ranked Sean Cody (4.4 score × 75% off) above
 * NakedSword (4.6 score × smaller deal) — but NakedSword is the
 * higher-converting destination overall, so optimizing the hero pick
 * for "biggest visible discount" leaves money on the table.
 *
 * Formula: hero_score = (overall_score * 0.6) + (deal_discount / 100 * 0.4)
 * Example: NakedSword 4.6 + 40% → 4.6*0.6 + 0.4*0.4 = 2.76 + 0.16 = 2.92
 *          Sean Cody  4.4 + 75% → 4.4*0.6 + 0.75*0.4 = 2.64 + 0.30 = 2.94
 *          (Sean Cody still wins here narrowly; once deals normalize
 *           the higher-scored site rises to the top — which is the point.)
 *
 * TODO: replace the heuristic with CTR-weighted selection once
 * Phase 4 click + impression data accumulates per destination.
 */
export function getTopDealPick(): SiteData | null {
  const affiliated = sites.filter(s => isAffiliated(s) && !isPendingReview(s) && !isEditorialOnly(s));
  if (affiliated.length === 0) return null;
  const withDeals = affiliated.filter(s => s.deal_discount > 0);
  if (withDeals.length > 0) {
    const heroScore = (s: SiteData) =>
      s.overall_score * 0.6 + (s.deal_discount / 100) * 0.4;
    return [...withDeals].sort((a, b) => heroScore(b) - heroScore(a))[0];
  }
  // Fallback: highest-scoring affiliated promotable site, else highest-scoring affiliated.
  const promotable = getPromotableSites();
  const pool = promotable.length > 0 ? promotable : affiliated;
  return [...pool].sort((a, b) => b.overall_score - a.overall_score)[0];
}
